<h1>プライバシーポリシー</h1>
<p>株式会社防災アプリ（以下「当社」といいます。）は、当社が提供するWebサービス「防サイトーク」（以下「本サービス」といいます。）の利用に際して、ユーザーから提供された個人情報（個人情報保護法に定める個人情報に限らず、個人に関するすべてのデータを含みます）を適切に取り扱い、保護することが重要であると考えています。当社は、本プライバシーポリシーを定め、本サービスを通じて提供される情報の収集・利用・管理について、ユーザーにご説明いたします。</p>
<h2>第1条 個人情報の定義</h2>
<p>本プライバシーポリシーにおいて、個人情報とは、ユーザー個人に関する情報であって、当該情報に含まれる氏名、生年月日、住所、電話番号、メールアドレス、その他の記述等により当該ユーザーを識別することができるものをいいます。</p>
<h2>第2条 個人情報の利用目的</h2>
<p>当社は、本サービスを通じて提供される個人情報を、以下の目的にのみ利用いたします。
<ol>
<li>本サービスの提供、本サービスの改善、ユーザーへのお知らせ、ユーザーからのお問い合わせへの対応等、本サービスの利用に係る目的
<li>当社が提供する他のサービスの提供、当社の商品・サービスに関する情報提供、当社のマーケティング活動等、当社の事業活動に係る目的
<li>ユーザーの同意がある場合、または法令に基づく場合、当社はユーザーの個人情報を第三者に提供することがあります。
</ol>
</p>
<h2>第3条 個人情報の管理</h2>
<p>当社は、個人情報の漏洩、滅失、毀損等を防止するため、合理的な安全対策を講じます。また、当社は、個人情報の取扱いを外部に委託する場合には、個人情報の保護に十分な水準を満たす委託先を選定し、適切な契約を締結するとともに、適切な監督を行います。</p>
<h2>第4条 個人情報の開示・訂正・利用停止等</h2>
<p>ユーザーは、当社に提供した個人情報について、開示、訂正、利用停止等を要求することができます。当社は、ユーザーから当該要求があった場合には、合理的な期間内に、ユーザーの要求に応じて対応いたします。</p>
<h2>第5条 本プライバシーポリシーの変更</h2>
<p>当社は、本プライバシーポリシーを必要に応じて変更することがあります。変更後の本プライバシーポリシーは、当社が本サービス上に掲載することによって、ユーザーに提供されるものとします。ユーザーは、本プライバシーポリシーの最新の内容を定期的にご確認ください。</p>
<h2>第6条 準拠法・管轄</h2>
<p>本プライバシーポリシーは、日本法に準拠するものとします。本サービスに係る紛争については、東京地方裁判所を第一審の専属管轄裁判所とします。</p>
